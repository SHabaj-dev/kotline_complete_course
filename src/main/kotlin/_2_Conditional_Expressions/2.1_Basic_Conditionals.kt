// 1. Basic Conditionals
/*
    - Conditional is a control flow statement that allows us to execute a block of code based on a condition.
    - There are two main types of conditionals:
        - if-else
        - when


 */
 

 // 1.1 if-else
 /*
    - if-else is a control flow statement that allows us to execute a block of code based on a condition.
    - if-else is used to check a single condition.
    - if is used to execute a block of code if the condition is true.
    - else is used to execute a block of code if the condition is false.
 */

 //Example:
//  fun main() {
//     val age = 18
//     if (age >= 18) {
//         println("You are an adult")
//     } else {
//         println("You are not an adult")
//     }
//  }

 //Output:
 //You are an adult


 // 1.2 when
 /*
    - when is a control flow statement that allows us to execute a block of code based on a condition.
    - when is used to check a single condition.
    - when is used to execute a block of code if the condition is true.
    - when is used to execute a block of code if the condition is false.
 */

 //Example:
//  fun main() {
//     val age = 18
//     when (age) {
//         in 18..100 -> println("You are an adult")
//         else -> println("You are not an adult")
//     }
//  }

 //Output:
 //You are an adult